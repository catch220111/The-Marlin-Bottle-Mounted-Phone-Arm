asm_of([("webbing", webF, C_WEB), ("stitches", stitchF, C_WASH),
        ("tpu_liner", patchF, C_TPU), ("slider", sliderF, C_DARK)],
       "01_strap_flat")

# 02 — strap curled
asm_of([("webbing", webC, C_WEB), ("tpu_liner", patchC, C_TPU)],
       "02_strap_curled")

# 03 — arm extended straight (+ open head)
asm_of(pose(0, 180, 180, -90) + [("spine", spine, C_DARK)],
       "03_arm_extended")

# 04 — arm folded nested stick (head caps the end)
asm_of(pose(-90, 0, 0, 0) + [("spine", spine, C_DARK)],
       "04_arm_folded_stick")

# 05 — full assembly, deployed zig-zag on bottle
webC2, patchC2 = strap_curled(ring_c)
asm_of(pose(-62, -157, -152, -55) +
       [("spine", spine, C_DARK)],
       "05_full_assembly_deployed",
       extra=[("bottle_ref", bottle, C_GHOST),
              ("webbing", webC2, C_WEB), ("tpu_liner", patchC2, C_TPU)])

# 06 — full assembly, folded stick on bottle
asm_of(pose(-90, 0, 0, 0) + [("spine", spine, C_DARK)],
       "06_full_assembly_folded",
       extra=[("bottle_ref", bottle, C_GHOST),
              ("webbing", webC2, C_WEB), ("tpu_liner", patchC2, C_TPU)])

# 07 — hinge stack-up, exploded along pin axis (joint 1)
a = cq.Assembly(name="07_hinge_stackup_exploded")
a.add(arm1, name="arm1_clevis_walls", color=C_AL)
a.add(wash1.translate((0, 22, 0)), name="friction_washers", color=C_WASH)
a.add(arm2.translate((0, 48, 0)), name="arm2_tongue", color=C_AL)
a.add(pin1.translate((0, 80, 0)), name="shoulder_pin_and_nut", color=C_PIN)
save(a, "07_hinge_stackup_exploded")

